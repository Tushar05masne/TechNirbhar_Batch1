package casting_Study;

public class Primitive_Cating {

	public static void main(String[] args) {
		
		//implicit---widening type-casting
		
		int i =200;
		
		double d=i;
		
		System.out.println(d);
		
		//================================//

		
		//explicit ---narrowing type-cating
		
		double d2=345.4556;
		
		int i2=(int) d2;
	
		System.out.println(i2);
		
		//================================//
		
		
		
		
		
		
		

	}

}
